#include <cstdio>
#include <vector>
using namespace std;
struct node {
	int tp, lv;
	node() {}
	node(int a, int b) {
		tp = a; lv = b;
	}
};
	int n, m, p;
vector<vector<node> > mp;
vector<vector<bool> > hasc;
vector<vector<int> > row;
vector<vector<int> > col;
vector<vector<bool> > rs; int res;
vector<vector<bool> > vs[4];
int inflv, inftp;
void dfs(int, int, int);
void go(int x, int y, int way, int tps) {
	
		if (way == 0) {
			if (y == m) return;
			else if (row[x][y] != tps) return;
			else dfs(x, y + 1, 0);
		}
		if (way == 1) {
			if (x == 1) return;
			else if (col[x - 1][y] != tps) return;
			else dfs(x - 1, y, 1);
		}
		if (way == 2) {
			if (y == 1) return;
			else if (row[x][y - 1] != tps) return;
			else dfs(x, y - 1, 2);
		}
		if (way == 3) {
			if (x == n) return;
			else if (col[x][y] != tps) return;
			else dfs(x + 1, y, 3);
		}
}
void dfs(int x, int y, int way) {
//	printf("st %d %d %d\n", x, y, way);
	if (vs[way][x][y]) return;
	else vs[way][x][y] = 1;
	int type;
	if (way == 0) type = row[x][y - 1];
	if (way == 1) type = col[x][y];
	if (way == 2) type = row[x][y];
	if (way == 3) type = col[x - 1][y];
	if (type == 0) return; // Null Road
	if (hasc[x][y]) {
		// Ate Chess
		if (mp[x][y].lv > inflv || mp[x][y].tp == inftp) return;
		else {
			if (rs[x][y] == 0) {
				res++;
				rs[x][y] = 1;
			}
			return;
		}
	}
	if (rs[x][y] == 0) { // Correct
		res++;
		rs[x][y] = 1;
//		printf("gets %d %d %d\n", x, y, way);
	}
	if (type == 1) return; // Single Road
	if (type == 2) {
		// Line Road
		if (way == 0) go(x, y, 0, 2);
		if (way == 1) go(x, y, 1, 2);
		if (way == 2) go(x, y, 2, 2);
		if (way == 3) go(x, y, 3, 2);
	}
	if (type == 3) {
		//Any Road
		go(x, y, 1, 3), go(x, y, 3, 3);
		go(x, y, 2, 3), go(x, y, 0, 3);
	}
}
void calc(int x, int y) {
	if (y < m) dfs(x, y + 1, 0);
	if (x > 1) dfs(x - 1, y, 1);
	if (y > 1) dfs(x, y - 1, 2);
	if (x < n) dfs(x + 1, y, 3);
}
